public class Vectort4i {
	final int a, b, c, d;

	public Vectort4i(int a, int b, int c, int d) {
		this.a = a;
		this.b = b;
		this.c = c;
		this.d = d;
	}

	public int getA() { return a; }

	public int getB() { return b; }

	public int getC() { return c; }

	public int getD() { return d; }
}
